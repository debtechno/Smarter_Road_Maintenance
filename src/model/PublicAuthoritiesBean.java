package model;

public class PublicAuthoritiesBean {

	private String org_name;
	private String org_id;
	private String ofc_adr;
	private String jur_area;
	public String getOrg_name() {
		return org_name;
	}
	public void setOrg_name(String org_name) {
		this.org_name = org_name;
	}
	public String getOrg_id() {
		return org_id;
	}
	public void setOrg_id(String org_id) {
		this.org_id = org_id;
	}
	public String getOfc_adr() {
		return ofc_adr;
	}
	public void setOfc_adr(String ofc_adr) {
		this.ofc_adr = ofc_adr;
	}
	public String getJur_area() {
		return jur_area;
	}
	public void setJur_area(String jur_area) {
		this.jur_area = jur_area;
	}
	
}
